export function getUiLanguage(id: string | null | undefined): UiLanguage {
  return uiLanguages.some((language) => language.id === id)
    ? (id as UiLanguage)
    : defaultUiLanguage;
}

export function getUiLanguageDirection(language: UiLanguage) {
  return uiLanguages.find((option) => option.id === language)?.dir ?? "ltr";
}

export function getUiLanguageName(language: UiLanguage) {
  const option = uiLanguages.find((item) => item.id === language);
  return option?.nativeLabel ?? option?.label ?? language;
}

export function t(
  language: UiLanguage,
  key: UiTextKey,
  values: Record<string, string | number> = {}
) {
  const template = dictionaries[language][key] ?? en[key];
  return Object.entries(values).reduce(
    (current, [name, value]) => current.replaceAll(`{${name}}`, String(value)),
    template
  );
}
